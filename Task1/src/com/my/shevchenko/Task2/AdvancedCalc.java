package com.my.shevchenko.Task2;

public class AdvancedCalc extends Calculator {

	@Override
	public int add(int x1, int x2) {
		return super.add(x1, x2);
	}

	@Override
	public int add(int x1, int x2, int x3) {
		return super.add(x1, x2, x3);
	}

	@Override
	public int sub(int x1, int x2) {
		return super.sub(x1, x2);
	}

	@Override
	public int sub(int x1, int x2, int x3) {
		return super.sub(x1, x2, x3);
	}
}
